    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        ColC produc = new ColC();
        produc.setNombre(nom_field.getText());
        produc.setApellido(ap_field.getText());
        produc.setDirección(dic_field.getText());
        produc.setTelefono(Integer.parseInt(tel_field.getText()));
        produc.setCarrera(car_field.getText());

        if(map.containsKey(cod_field.getText())==false){
            map.put(cod_field.getText(), produc);
            JOptionPane.showMessageDialog(null,"Estudiante registrado");
        }else{
            MOSTRAR.setText("Estudiante no registrado");
        }

        nom_field.setText("");
        ap_field.setText("");
        dic_field.setText("");
        tel_field.setText("");
        car_field.setText("");
        cod_field.setText("");
        MOSTRAR.setText("");

    }//GEN-LAST:event_jButton1ActionPerformed

