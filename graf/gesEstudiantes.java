    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        // TODO add your handling code here:
        String codB = JOptionPane.showInputDialog("Ingrese codigo del estudiante");
        if(map.containsKey(codB)==true){
            ColC producto =map.get(codB);
            String nom= nom_field2.getText();
            String ap= ap_field2.getText();
            String dic = dic_field2.getText();
            String tel= tel_field2.getText();
            String car= car_field2.getText();
            producto.setNombre(nom);
            producto.setApellido(ap);
            producto.setDirección(dic);
            producto.setTelefono(Integer.parseInt(tel));
            producto.setCarrera(car);
            String mensaje = "Datos actualizado\n";

            MOSTRARA.setText(mensaje);
        }else{
            MOSTRARA.setText("No registrado");
        }
    }//GEN-LAST:event_jButton5ActionPerformed

